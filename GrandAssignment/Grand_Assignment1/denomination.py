n = int(input())
k = int( n / 1000)
rk = n - (k * 1000)
fh = int( rk / 500 )
rfh = rk - ( fh * 500)
h = int( rfh / 100)
rh = rfh - ( h * 100)
ft = int( rh / 50)
rft = rh - ( ft * 50)
t = int( rft / 20)
rt = rft - ( t * 20)
f = int( rt / 5)
rf = rt - (f * 5)
print("1000:"+str(k))
print("500:"+str(fh))
print("100:"+str(h))
print("50:"+str(ft))
print("20:"+str(t))
print("5:"+str(f))
print("1:"+str(rf))